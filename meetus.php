                    <center><strong><h2>EN RUBÍ TAMBIÉN PODEMOS</h2></strong></center><br><br>
                    <div style="text-align: justify;">
                        En los últimos años estamos viendo como multitud de movimientos sociales e iniciativas ciudadanas están
                        denunciando la incapacidad de la vieja política para dar respuestas a
                        las necesidades de los ciudadanos. Desde el poder sólo vemos arrogancia
                        e impunidad con los corruptos para que todo siga igual. Las
                        instituciones han acabado estando al servicio de los partidos políticos
                        y no de sus ciudadanos. Hay un clamor popular para que esto cambie,
                        para que la democracia y sus instituciones se dignifiquen y cumplan el
                        papel para las que fueron creadas.<br>
                        <br>
                        En democracia los ciudadanos deben poder optar a la gestión pública. No
                        podemos dejar en manos de los de siempre la capacidad de decidir sobre
                        todas aquellas cuestiones que nos atañen. La ciudadanía ha de ocupar su
                        espacio en la política, devolver el poder de decisión a los ciudadanos
                        y elegir representantes honestos y capaces que están al servicio de
                        ellos. Necesitamos recuperar la democracia al servicio de las personas.<br>
                        <br>
                        Desde <strong>Podemos-Rubí</strong> queremos trabajar en esa dirección
                        y lo queremos hacer con los vecinos, hablando con ellos, escuchando sus
                        inquietudes y sugerencias. El protagonismo debe volver a la calle y
                        salir de los despachos. No necesitamos políticos que tengan miedo a
                        salir y hablar con sus vecinos. <strong>Amamos Rubí pero no nos gusta el estado lamentable en que lo han dejado los actuales políticos.</strong>
                        Los problemas actuales que padecemos no los resolverín los mismos que
                        los han creado. La política necesita una verdadera regeneración
                        democrática y debe empezar por dotarnos de mecanismos que impidan la
                        corrupción.<br>
                        <br>
                        Queremos que la honestidad de sus gobernantes está fuera de toda duda y
                        que den la cara en todo momento. Hay que poner fin a la acumulación de
                        cargos, limitar los sueldos y mandatos, hacer accesible la gestión del
                        Ayuntamiento a todos los ciudadanos, que existan agendas transparentes
                        de los políticos con responsabilidades, que los plenos puedan ser
                        visionados por todos y que podamos ver el trabajo de nuestros
                        representantes.<br>
                        <br>
                        Los ciudadanos deben poder decidir en aquellas grandes cuestiones de
                        transcendencia municipal que nos afecten. Hay que habilitar métodos de
                        participación y votación. Los representantes deben estar fiscalizados
                        en todo momento por sus ciudadanos. Queremos decidir cómo debe ser el
                        Rubí que necesitamos y deseamos. Queremos un Rubí sin desahucios, sin
                        malnutrición entre los más pequeños, donde se garanticen el derecho a
                        la educación, la sanidad y a una renta mínima, donde nuestros impuestos
                        se correspondan realmente con los servicios que recibimos. No queremos
                        instituciones prisioneras de los préstamos de los Bancos y entidades
                        financieras que impidan una autonomía en las decisiones. <strong>Hay que erradicar las puertas giratorias,</strong> también en el ámbito municipal.<br>
                        <br>
                        Estamos cansados de que se secuestre el presupuesto del Ayuntamiento
                        para realizar obras justo antes de las elecciones. No somos ingenuos,
                        queremos una verdadera gestión que dure cuatro años y que cada día sea
                        un día de trabajos realizados. Ya está bien de utilizar obras y
                        proyectos necesarios para fines políticos.<br>
                        <br>
                        Los responsables políticos tienen que trabajar pensando en el futuro de
                        las siguientes generaciones, no en el resultado de las próximas
                        elecciones. Queremos una institución que incorpore criterios sociales,
                        ambientales y de calidad en todas sus acciones.<br>
                        <br><br>
                        <strong>La transparencia en la política Local.</strong><br>
                        <br>
                        El Ayuntamiento es la Administración más cercana a los ciudadanos y
                        como tal debe prestar especial atención a su transparencia. Es
                        necesario abrir las puertas y ventanas de la administración local. La
                        cercanía entre los ciudadanos y sus representantes es vital para
                        conseguir esto.<br>
                        <br>
                        Para ello es necesario una auditoría de ingresos y gastos del
                        Ayuntamiento de Rubí de los últimos años. Una auditoría del organigrama
                        del Ayuntamiento y una adecuación a las verdaderas necesidades de los
                        ciudadanos, para conseguir una mayor eficiencia. Publicación en la
                        página web de todos los sueldos públicos y, en particular, de los
                        cargos electos y asesores que pudieran tener estos. Publicación
                        detallada en la web de todos los ingresos y gastos del Ayuntamiento,
                        así como el acceso de los ciudadanos a todas las facturas pagadas con
                        fondos públicos, evitando con dicha transparencia casos en los que
                        empresas de familiares de cargos políticos obtienen contratos con la
                        Administración municipal de forma sospechosa. No a la acumulación de
                        cargos políticos.<br>
                        <br>
                        Las agendas del Alcalde y regidores deben ser públicas y transparentes
                        para que los ciudadanos puedan ver a qué dedican el tiempo por el cual
                        cobran un sueldo, que les pagamos todos los ciudadanos, para que nos
                        representen.<br>
                        <br>
                        Los medios tecnológicos actuales deben ser puestos al servicio de la
                        ciudadanía. Los Plenos Municipales deben ser públicos y facilitar las
                        preguntas y exposiciones a aquellos ciudadanos que quieran exponer un
                        problema. Para ello la grabación de los Plenos y su posterior
                        exposición en la web municipal, para que puedan ser visionados por
                        todos los ciudadanos, es una medida necesaria que permitiría a muchas
                        personas ver a sus representantes y poder tener un mayor conocimiento
                        de ellos. No debería haber nada que ocultar.<br>
                        <br>
                        Especial atención a la transparencia de los concursos y licitaciones y
                        establecimiento de mecanismos de control. Evitar las adjudicaciones
                        directas sin concurso público abierto, aun cuando la ley lo permita por
                        la cuantía del contrato, y sólo dejar esta posibilidad para contratos
                        de obras de emergencia.<br>
                        <br>
                        Es necesario un compromiso ético vinculante para todos aquellos que
                        aspiran a representarnos. Un código ético entre ciudadanía y
                        representantes.<br>
                        <br>
                        Reducción drástica de los gastos en las campañas electorales.<br>
                        <br>
                        Es hora de echar a los corruptos de las instituciones. Tolerancia cero ante la corrupción. <br>
                        <br><br>
                        <strong>Participación activa y directa de los ciudadanos en la gestión municipal.</strong><br>
                        <br>
                        Queremos promover referéndums en temas de especial trascendencia para
                        los ciudadanos. El que los ciudadanos puedan decidir sobre aquellas
                        grandes cuestiones que les afecten no debería ser una excepcionalidad
                        sino el resultado de una normalidad democrática. Se deben crear
                        Consejos temáticos donde participen políticos, técnicos, organizaciones
                        sociales, AA.VV y ciudadanos en general, para ello es necesario
                        establecer un protocolo de actuación en este sentido.<br>
                        <br>
                        La Administración Local debe crear vínculos con las asociaciones de
                        Rubí para impulsar la participación, hacer de nuestra ciudad un foro
                        donde las ideas e inquietudes fluyan en beneficio de todos. Hay que dar
                        la palabra a las personas. La política está para trabajar y dar
                        soluciones al bienestar de la ciudadanía.<br>
                        <br><br>
                        <strong>Recuperar la capacidad en la gestión municipal.</strong><br>
                        <br>
                        Recuperar la gestión de servicios que hoy están en otras manos y que
                        encarecen innecesariamente la factura municipal. Caminar hacia la
                        municipalización del servicio de agua como elemento básico para las
                        personas y no de negocio. Dotar a la infraestructura del Ayuntamiento
                        de recursos para ser eficientes. Los fondos públicos deben ser
                        gestionados con la máxima eficiencia. La dejadez del Ayuntamiento en
                        sus funciones, durante estos años, ha sido patente en la falta de
                        limpieza, desbroce, cuidado y mantenimiento de plazas y calles,
                        recortes en el transporte público, etc.<br>
                        <br>
                        Desde Podemos-Rubí somos conscientes de que no hay fórmulas mágicas y
                        no queremos caer en la tentación de realizar programas electorales que
                        se conviertan meramente en un listado de cosas incumplidas. Queremos
                        trabajar humildemente con los movimientos sociales y vecinales que
                        llevan tiempo en esta tarea, y construir un glosario de ideas y
                        soluciones. ¿Quién mejor que los vecinos para saber qué sucede y qué es
                        lo que queremos?<br>
                        <br>
                        Para ello necesitamos que apostéis por la participación sin paliativos. <strong>Podemos Rubí</strong>
                        iniciará un proceso de información y debate con la ciudadanía para
                        detectar aquellos problemas que nos preocupan y buscar soluciones entre
                        todos. Es la hora de la ciudadanía y de hacer aquello que realmente
                        temen los poderes fácticos actuales: que los ciudadanos accedan a la
                        política para mejorar la situación de la mayoría de personas, en lugar
                        de favorecer a las élites económicas y políticas.<br>
